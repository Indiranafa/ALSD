public class Layanan14 {
    int kodeLayanan;
    String namaLayanan;
    int harga;

    public Layanan14(int kodeLayanan, String namaLayanan, int harga) {
        this.kodeLayanan = kodeLayanan;
        this.namaLayanan = namaLayanan;
        this.harga = harga;
    }

    public int getKodeLayanan() {
        return kodeLayanan;
    }

    public String getNamaLayanan() {
        return namaLayanan;
    }

    public int getHarga() {
        return harga;
    }
}
