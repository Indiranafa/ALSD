public class Pelanggan14 {
    String namaPelanggan;
    String noHp;

    public Pelanggan14(String namaPelanggan, String noHp) {
        this.namaPelanggan = namaPelanggan;
        this.noHp = noHp;
    }

    public String getNamaPelanggan() {
        return namaPelanggan;
    }

    public String getNoHp() {
        return noHp;
    }
}
